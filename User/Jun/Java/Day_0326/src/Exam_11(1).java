//제어문(if(조건식) ~else, switch case)
public class Exam_11 {

	public static void main(String[] args) {
		int a=97, b=88;
		int max,min;
		if(a>b) {
			max=a;
			min=b;
		
		}
		else if(a<b) {
			max=b;
			min=a;
		
		}
		else{
			max=b;
			min=a;
		
		}
	System.out.println("max:"+max);
	System.out.println("min:"+min);
	}
	
	
}
	
