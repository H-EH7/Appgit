public class Exam_23 {

	public static void main(String[] args) {
		//for()
		int sum =0;//
		for(int x=1; x<=10; x++) {
			sum += x;
		}
		System.out.println("SUM="+sum);
		int cnt = 0;
		int tot = 0;
		
		while(cnt<10) {
			cnt++;
			tot+=cnt;
			
		}
		System.out.println("TOT="+tot);
		

	}

}
